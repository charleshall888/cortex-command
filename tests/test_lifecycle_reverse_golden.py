def _run_state_cli(
    repo: Path, slug: str, monkeypatch: pytest.MonkeyPatch, capsys: pytest.CaptureFixture[str]
) -> tuple[str, str]:
    """cortex-lifecycle-state reader — returns (stdout, stderr)."""
    monkeypatch.chdir(repo)
    with pytest.raises(SystemExit) as exc:
        state_cli_mod.main(["--feature", slug])
    assert exc.value.code == 0
    cap = capsys.readouterr()
    return cap.out, cap.err


def _run_counters(
    lifecycle_dir: Path, slug: str, capsys: pytest.CaptureFixture[str]
) -> str:
    """cortex-lifecycle-counters reader — returns stdout."""
    rc = counters_mod.main(["--feature", slug, "--lifecycle-dir", str(lifecycle_dir)])
    assert rc == 0
    return capsys.readouterr().out


def _run_dashboard(lifecycle_dir: Path, slug: str) -> str | None:
    """dashboard/data.py reader — returns the resolver-derived current_phase."""
    return parse_feature_events(slug, lifecycle_dir)["current_phase"]


def _run_generate_index(repo: Path, lifecycle_dir: Path, slug: str) -> str | None:
    """generate_index.py reader — stage a backlog item pointing at slug, return
    the item's resolver-derived lifecycle_phase (base phase, -paused stripped)."""
    backlog_dir = repo / "cortex" / "backlog"
    backlog_dir.mkdir(parents=True, exist_ok=True)
    (backlog_dir / f"9001-{slug}.md").write_text(
        f"---\nid: 9001\ntitle: Reverse golden probe\nstatus: ready\n"
        f"lifecycle_slug: {slug}\n---\n\nprobe body\n",
        encoding="utf-8",
    )
    active_items, _active_ids, _archive_ids, _all_items = generate_index.collect_items(
        backlog_dir=backlog_dir, lifecycle_dir=lifecycle_dir
    )
    matches = [it for it in active_items if it["id"] == 9001]
    assert matches, "backlog probe item not collected"
    return matches[0]["lifecycle_phase"]


def _run_scan_lifecycle(
    repo: Path, slug: str, monkeypatch: pytest.MonkeyPatch, capsys: pytest.CaptureFixture[str]
) -> str:
    """scan_lifecycle reader — drive main() end-to-end, return additionalContext.

    Single incomplete feature + no session match → the hook crash-recovery
    claims it active and renders its phase label; a terminal feature is
    suppressed and the hook emits nothing (additionalContext == "").
    """
    payload = {"hook_event_name": "SessionStart", "session_id": "REV-SID", "cwd": str(repo)}
    env_file = repo / ".claude-env"
    env_file.write_text("", encoding="utf-8")
    monkeypatch.setenv("CLAUDE_ENV_FILE", str(env_file))
    monkeypatch.delenv("LIFECYCLE_SESSION_ID", raising=False)
    monkeypatch.setenv("CORTEX_SCAN_LIFECYCLE_STALE_DAYS", "0")
    monkeypatch.setattr("sys.stdin", io.StringIO(json.dumps(payload)))
    rc = scan_lifecycle_mod.main()
    assert rc == 0
    return extract_additional_context(capsys.readouterr().out)


def _run_statusline(lifecycle_dir: Path, slug: str) -> str:
    """statusline.sh reader — invoke the bash phase ladder against the staged dir.

    The statusline is a bash reader and a permanent artifact-derivation exception
    (it is NOT migrated to the events-first resolver). It is exercised
    deterministically here by sourcing its phase-detection ladder fragment
    (shared with the parity harness in ``tests.test_lifecycle_phase_parity``)
    against the staged feature dir — not covered only-by-proxy.
    """
    return _invoke_statusline_ladder(lifecycle_dir / slug).strip()


