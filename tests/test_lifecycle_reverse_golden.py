# ---------------------------------------------------------------------------
# Inertness differential (anti-self-seal): mixed-log projection == additive-free
# legacy-log projection, for every reader. Independent of the pinned oracles.
# ---------------------------------------------------------------------------


@pytest.mark.parametrize("case", CASES)
def test_additive_rows_are_inert(case, tmp_path, monkeypatch, capsys):
    mixed = _read(case, "events.log")
    stripped = _strip_additive(mixed)
    # Sanity: stripping actually removed additive content for the machine cases.
    assert stripped != mixed or "advance_" not in mixed

    def projections(log_text: str, sub: Path):
        repo, lc, slug = _stage(case, sub, log_text=log_text)
        state_out, state_err = _run_state_cli(repo, slug, monkeypatch, capsys)
        return {
            "state_out": state_out,
            "counters": _run_counters(lc, slug, capsys),
            "dashboard": _run_dashboard(lc, slug),
            "generate_index": _run_generate_index(repo, lc, slug),
            "statusline": _run_statusline(lc, slug),
        }

    mixed_proj = projections(mixed, tmp_path / "mixed")
    legacy_proj = projections(stripped, tmp_path / "legacy")
    assert mixed_proj == legacy_proj, (
        f"{case}: additive machine content changed a legacy projection\n"
        f"  mixed  = {mixed_proj}\n  legacy = {legacy_proj}"
    )


