# ---------------------------------------------------------------------------
# Enumerated reader set (build-time grep — spec R16 "enumerated by grep")
# ---------------------------------------------------------------------------
#
# (label, repo-relative reader path, sentinel symbol proving it is a
# reducer/resolver consumer of events.log). The fixture README pins the same
# set; ``test_readme_pins_enumerated_readers`` asserts this table, the README,
# and the live source agree — so a reader that drops its resolver/reducer call,
# or a README that drops a reader, fails CI.
ENUMERATED_READERS: tuple[tuple[str, str, str], ...] = (
    ("cortex-lifecycle-state", "cortex_command/lifecycle/state_cli.py", "reduce_lifecycle_state"),
    ("cortex-lifecycle-counters", "cortex_command/lifecycle/counters.py", "count_rework_cycles"),
    ("statusline derivation", "claude/statusline.sh", "detect_lifecycle_phase"),
    ("dashboard/data.py", "cortex_command/dashboard/data.py", "resolve_lifecycle_phase"),
    ("scan_lifecycle", "cortex_command/hooks/scan_lifecycle.py", "resolve_lifecycle_phase"),
    ("generate_index.py", "cortex_command/backlog/generate_index.py", "resolve_lifecycle_phase"),
)


def enumerate_reader_sites() -> list[tuple[str, str, int, str]]:
    """Grep each enumerated reader for its sentinel; return (label, path, lineno, line).

    Deterministic recapture harness for the README's reader table: run this and
    paste the ``path:lineno`` values. Fails loudly if a sentinel vanished from a
    reader (the reader stopped consuming the shared resolver/reducer).
    """
    sites: list[tuple[str, str, int, str]] = []
    for label, rel, sentinel in ENUMERATED_READERS:
        text = (REPO_ROOT / rel).read_text(encoding="utf-8")
        hit = None
        for lineno, line in enumerate(text.splitlines(), start=1):
            if sentinel in line:
                hit = (lineno, line.strip())
                break
        assert hit is not None, (
            f"reader {label} ({rel}) no longer references sentinel {sentinel!r} "
            f"— the enumerated reader set is stale, re-derive it"
        )
        sites.append((label, rel, hit[0], hit[1]))
    return sites


