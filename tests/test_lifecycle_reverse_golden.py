def test_reader_sentinels_present():
    """Every enumerated reader still calls its shared resolver/reducer sentinel."""
    sites = enumerate_reader_sites()
    assert len(sites) == len(ENUMERATED_READERS)


def test_readme_pins_enumerated_readers():
    """The fixture README enumerates exactly the grepped reader set, and states
    the prose-embedded-readers carve-out."""
    readme = (FIXTURE_DIR / "README.md").read_text(encoding="utf-8")
    for label, rel, _sentinel in ENUMERATED_READERS:
        assert label in readme, f"README missing reader label {label!r}"
        assert rel in readme, f"README missing reader path {rel!r}"
    # Prose-embedded readers explicitly out of scope (coherence req 1).
    assert "prose-embedded" in readme.lower()
    assert "legacy display-phase projection" in readme.lower()
