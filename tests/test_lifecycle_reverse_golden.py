"""Reverse-direction golden — old readers over machine-written mixed logs.

Spec 374 R16 (arm h). Phase 4 (phase-authority cutover, ADR-0025) forfeits the
cheap prose-side git-revert rollback; the STANDING safety net is that OLD READERS
keep working when they encounter machine-written logs. This golden pins that
guarantee: machine-written **mixed** logs (legacy transition rows —
``lifecycle_start`` / ``phase_transition`` / ``review_verdict`` / approvals /
terminal events — interleaved with the additive machine rows ``advance_started`` /
``advance_committed`` and the additive ``invocation_id`` field the ``advance``
dual-emission carries) are fed to the enumerated legacy reducer consumers, and
each reader's projection is asserted to stay CORRECT. This is the "reverse
direction": new writer, old reader.

The enumerated reader set is pinned in the fixture-directory README (see
FIXTURE_DIR below) and re-derived by grep here
(:func:`enumerate_reader_sites`) so the list stays honest. Prose-embedded readers
are explicitly NOT covered by this golden — their compatibility surface is the
served envelope's legacy display-phase projection field (coherence req 1), stated
in the fixture README.

"Correct legacy-phase projection" means the ACTUAL current behavior after Task 17
migrated three of these readers to the events-first shared resolver
(``common.resolve_lifecycle_phase``): where machine rows exist, the events-derived
state is authoritative; the statusline keeps its own artifact-only derivation (a
permanent, parity-pinned exception), so a mixed log can legitimately project
DIFFERENTLY between the events-first Python readers and the statusline (the
``rev-rework-events-only`` case pins exactly that divergence).

Anti-self-seal note: the ``*.resolver.phase`` / ``*.state.stdout`` / etc.
expected files are HAND-AUTHORED oracles reasoned from the documented legacy
ladder — NOT regenerated by running the readers. Independently, every case is
also checked with its additive machine content STRIPPED
(:func:`_strip_additive`): each reader's projection over the mixed log must equal
its projection over the additive-free legacy log, proving the additive rows and
the ``invocation_id`` field are inert (not merely that the reader agrees with
itself).
"""

