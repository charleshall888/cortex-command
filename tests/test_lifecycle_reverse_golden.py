CASES: list[str] = sorted(
    p.name[: -len(".events.log")] for p in FIXTURE_DIR.glob("*.events.log")
)

# events the advance dual-emission adds on top of the legacy vocabulary; the
# reverse golden proves these are inert to every old reader.
_ADDITIVE_EVENTS = frozenset({"advance_started", "advance_committed"})


def _read(case: str, suffix: str) -> str | None:
    p = FIXTURE_DIR / f"{case}.{suffix}"
    return p.read_text(encoding="utf-8") if p.is_file() else None


def _strip_additive(log_text: str) -> str:
    """Return the log with additive machine content removed.

    Drops every ``advance_started`` / ``advance_committed`` row and strips the
    additive ``invocation_id`` field from the surviving (legacy-vocabulary) rows,
    reconstructing the pre-cutover legacy log. Torn/unparseable lines pass through
    verbatim so the tolerant-reader behavior is preserved. Used only by the
    inertness differential — never to build the pinned expectations.
    """
    out: list[str] = []
    for raw in log_text.splitlines():
        line = raw.strip()
        if not line:
            out.append(raw)
            continue
        try:
            ev = json.loads(line)
        except json.JSONDecodeError:
            out.append(raw)  # torn line — keep verbatim
            continue
        if isinstance(ev, dict) and ev.get("event") in _ADDITIVE_EVENTS:
            continue
        if isinstance(ev, dict) and "invocation_id" in ev:
            ev = {k: v for k, v in ev.items() if k != "invocation_id"}
            out.append(json.dumps(ev))
        else:
            out.append(raw)
    return "".join(s + "\n" for s in out)


def _stage(case: str, tmp_path: Path, *, log_text: str | None = None) -> tuple[Path, Path, str]:
    """Stage a scratch repo for ``case``; return (repo_root, lifecycle_dir, slug).

    The slug is the fixture basename (a synthetic ``rev-*`` name that never
    exists in the real ``cortex/lifecycle/`` tree). ``log_text`` overrides the
    staged events.log (used by the inertness differential); otherwise the
    fixture ``.events.log`` is staged verbatim.
    """
    slug = case
    repo = tmp_path / "repo"
    lifecycle_dir = repo / "cortex" / "lifecycle"
    fdir = lifecycle_dir / slug
    fdir.mkdir(parents=True, exist_ok=True)

    events = log_text if log_text is not None else _read(case, "events.log")
    assert events is not None, f"{case}.events.log missing"
    (fdir / "events.log").write_text(events, encoding="utf-8")

    for suffix, dest in (
        ("research.md", "research.md"),
        ("spec.md", "spec.md"),
        ("plan.md", "plan.md"),
        ("review.md", "review.md"),
    ):
        content = _read(case, suffix)
        if content is not None:
            (fdir / dest).write_text(content, encoding="utf-8")

    return repo, lifecycle_dir, slug


