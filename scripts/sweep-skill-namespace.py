# ---------------------------------------------------------------------------
# Carve-outs: (relative-path, 1-based line number, exact substring to skip)
#
# Each entry suppresses rewriting on the named line if the line contains the
# given substring. Use the smallest substring that uniquely matches the
# historical-input occurrence so we do NOT skip other rewrites on the same
# line.
# ---------------------------------------------------------------------------

CARVE_OUTS: tuple[tuple[str, int, str], ...] = (
    # Validation example for empty-after-sanitization input.
    ("skills/retro/SKILL.md", 38, '"/cortex:retro ---"'),
    # Sanitization example for invalid-character input.
    ("skills/retro/SKILL.md", 43, "`/cortex:retro !!!`"),
)


# ---------------------------------------------------------------------------
# Rewrite logic
# ---------------------------------------------------------------------------

# Match `/cortex:<skill>` where <skill> starts with a lowercase letter and is
# composed of [a-z0-9-]. Uses a leading negative-lookbehind to avoid matching
# inside identifiers (e.g. `xx/cortex:foo` is unlikely but the verifier uses
# the same guard, so stay consistent).
OLD_FORM_RE = re.compile(
    r"(?<![A-Za-z0-9_-])/cortex:([a-z][a-z0-9-]*)"
)


def line_has_carveout(rel_path: str, lineno: int) -> tuple[bool, str | None]:
    """Return (True, needle) if the line is carved out, else (False, None)."""
    for cf, cl, needle in CARVE_OUTS:
        if cf == rel_path and cl == lineno:
            return True, needle
    return False, None


def rewrite_line(rel_path: str, lineno: int, line: str) -> tuple[str, int]:
    """Return (new_line, replacements_made).

    If the line is carved out, the carve-out substring is preserved verbatim
    while other matches on the same line are still rewritten.
    """
    carved, needle = line_has_carveout(rel_path, lineno)
    if not carved:
        new, n = OLD_FORM_RE.subn(_sub, line)
        return new, n
    # Split the line on the carve-out needle and rewrite each non-carved
    # segment independently. If the needle isn't found (line numbers shifted),
    # fall through to a normal rewrite — verifier will catch the survivor.
    if needle is None or needle not in line:
        new, n = OLD_FORM_RE.subn(_sub, line)
        return new, n
    parts = line.split(needle)
    rewritten_parts: list[str] = []
    total = 0
    for i, part in enumerate(parts):
        new_part, n = OLD_FORM_RE.subn(_sub, part)
        rewritten_parts.append(new_part)
        total += n
    return needle.join(rewritten_parts), total


def _sub(match: re.Match[str]) -> str:
    skill = match.group(1)
    owner = SKILL_OWNER.get(skill)
    if owner is None:
        # Unknown skill — leave the original text intact. The verifier will
        # surface this as `unknown-skill` if it slipped through.
        return match.group(0)
    return f"/{owner}:{skill}"


def sweep_file(root: Path, rel_path: str) -> tuple[int, int]:
    """Rewrite a single file. Returns (lines_changed, replacements)."""
    abs_path = root / rel_path
    if not abs_path.exists():
        sys.stderr.write(f"[skip] missing: {rel_path}\n")
        return 0, 0
    text = abs_path.read_text(encoding="utf-8")
    out_lines: list[str] = []
    changed_lines = 0
    total_replacements = 0
    # Preserve trailing newline behavior by splitting with keepends.
    for lineno, line in enumerate(text.splitlines(keepends=True), start=1):
        # Strip the trailing newline for matching, then re-attach.
        if line.endswith("\r\n"):
            body, eol = line[:-2], "\r\n"
        elif line.endswith("\n"):
            body, eol = line[:-1], "\n"
        else:
            body, eol = line, ""
        new_body, n = rewrite_line(rel_path, lineno, body)
        if n > 0:
            changed_lines += 1
            total_replacements += n
        out_lines.append(new_body + eol)
    new_text = "".join(out_lines)
    if new_text != text:
        try:
            abs_path.write_text(new_text, encoding="utf-8")
        except PermissionError as exc:
            sys.stderr.write(
                f"[error] cannot write {rel_path}: {exc}. "
                "Edit manually and re-run with --verify-only.\n"
            )
            return changed_lines, total_replacements
    return changed_lines, total_replacements


def main(argv: list[str] | None = None) -> int:
    root = Path(__file__).resolve().parent.parent
    grand_lines = 0
    grand_repls = 0
    files_touched = 0
    for rel in IN_SCOPE_FILES:
        lines, repls = sweep_file(root, rel)
        if repls:
            files_touched += 1
            print(f"  {rel}: {lines} line(s), {repls} replacement(s)")
        grand_lines += lines
        grand_repls += repls
    print(
        f"sweep complete: {files_touched} files touched, "
        f"{grand_lines} lines changed, {grand_repls} replacements made"
    )
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
