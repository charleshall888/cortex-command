"""One-shot helper to sweep `/cortex:<skill>` -> `/cortex-<plugin>:<skill>` across
the in-scope source tree for ticket 122 (R8).

Rewrites every `/cortex:<skill>` reference in the listed files to use the
post-install plugin namespace based on the canonical mapping derived from
`justfile:423-432`:

  cortex-interactive            -> 14 skills
  cortex-overnight-integration  -> overnight, morning-review

Carve-outs (lines that should NOT be rewritten — they document literal
historical input strings, not invocations):

  skills/retro/SKILL.md:38 — "/cortex:retro ---" (validation example)
  skills/retro/SKILL.md:43 — "/cortex:retro !!!" (sanitization example)

Edits are applied to the top-level source tree only. Plugin trees
(`plugins/cortex-interactive/skills/`, `plugins/cortex-overnight-integration/skills/`)
are regenerated by `just build-plugin` in a follow-up task and MUST NOT be
edited here.

Stdlib-only. Idempotent: a second run is a no-op.
"""

